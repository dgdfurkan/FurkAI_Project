function urlBase64ToUint8Array(base64String) {
  const padding = '='.repeat((4 - base64String.length % 4) % 4);
  const base64 = (base64String + padding).replace(/-/g, '+').replace(/_/g, '/');
  const rawData = atob(base64);
  const outputArray = new Uint8Array(rawData.length);
  for (let i = 0; i < rawData.length; ++i) outputArray[i] = rawData.charCodeAt(i);
  return outputArray;
}

// Push aboneliƒüi olu≈ütur ve backend'e g√∂nder
async function ensurePushSubscription(reg) {
  let sub = await reg.pushManager.getSubscription();
  if (!sub) {
    sub = await reg.pushManager.subscribe({
      userVisibleOnly: true,
      applicationServerKey: urlBase64ToUint8Array(VAPID_PUBLIC_KEY)
    });
  }
  
  // Backend'e aboneliƒüi kaydet
  try {
    const response = await fetch('/api/subscribe', {
      method: 'POST',
      headers: {'content-type':'application/json'},
      body: JSON.stringify({ 
        subscription: sub, 
        timezone: Intl.DateTimeFormat().resolvedOptions().timeZone 
      })
    });
    
    if (response.ok) {
      console.log('‚úÖ Push aboneliƒüi backend\'e kaydedildi');
    } else {
      console.log('‚ùå Backend abonelik hatasƒ±');
    }
  } catch (error) {
    console.log('‚ùå Backend baƒülantƒ± hatasƒ±:', error);
  }
}

